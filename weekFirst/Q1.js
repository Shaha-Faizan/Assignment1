let marks = 50;

// greater than 90 then A Grade
if(marks>90){
    console.log("Grade A");
}
// if between 70 and 90 then B Grade
else if(marks>=70 && marks<=90){
    console.log("B Grade");
}
//if between 50 and 70 then C Grade
else if(marks>=50 && marks<=70){
    console.log("C Grade");
}
// Below 50 then F Grade
else{
    console.log("F Grade");
}
